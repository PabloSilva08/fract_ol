#include "push.h"

void swap_a(t_stk **a)
{
	t_stk *tmp_1;
	t_stk *tmp_2;

	tmp_1 = *a;
	tmp_2 = (*a)->next->next;
	*a = (*a)->next;
	(*a)->next = tmp_1;
	(*a)->next->next = tmp_2;
}
